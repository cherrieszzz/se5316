package haut.hati.zpy.bean;

public class Case02Bean {
    private double a, b, c;
    public Case02Bean(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }

    public double getC() {
        return c;
    }

}
